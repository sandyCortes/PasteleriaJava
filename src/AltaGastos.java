    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Connection c=Coneccion.conexion();
        if(c != null){
            try {
                CallableStatement cs =c.prepareCall("{call dbo.AgregaGasto(?,?,?,?,?)}");
                cs.setString(1, txtFecha.getText());
                cs.setString(2, cbxProducto.getSelectedItem().toString());
                cs.setInt(3,Integer.parseInt(txtCantidad.getText()));
                cs.setFloat(4,Float.parseFloat(txtMonto.getText()));
                cs.registerOutParameter(5, Types.VARCHAR);
                cs.execute();
                JOptionPane.showMessageDialog(null,cs.getString(5));
            } catch (Exception e) {
                System.out.println("Error en guardar gasto "+e);
            }
        } else
            System.out.println("No hay conexi√≥n a la BD");
    }//GEN-LAST:event_btnGuardarActionPerformed

